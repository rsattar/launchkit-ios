#import "SessionViewController.h"

#import "LaunchKit+DevSampleAdditions.h"

@interface SessionViewController ()

@property (strong, nonatomic) NSMutableArray *sessionParameterKeys;

@end

@implementation SessionViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
    self.sessionParameterKeys = [NSMutableArray arrayWithCapacity:20];
}

- (void)viewWillAppear:(BOOL)animated
{
    [super viewWillAppear:animated];
    [self.sessionParameterKeys removeAllObjects];
    [self.sessionParameterKeys addObjectsFromArray:[[LaunchKit sharedInstance].sessionParameters allKeys]];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    // Return the number of rows in the section.
    return self.sessionParameterKeys.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"sessionCellIdentifier" forIndexPath:indexPath];
    
    // Configure the cell...
    NSString *key = self.sessionParameterKeys[indexPath.row];
    cell.textLabel.text = key;
    cell.detailTextLabel.text = [NSString stringWithFormat:@"%@", [LaunchKit sharedInstance].sessionParameters[key]];
    
    return cell;
}

